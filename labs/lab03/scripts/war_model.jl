using DifferentialEquations, Plots, DelimitedFiles

# Начальные условия (твой вариант)
x0 = 120000
y0 = 90000
tspan = (0.0, 2.0)

# ============================================================
# 1. Модель регулярных войск
# ============================================================
function regular!(du, u, p, t)
    x, y = u
    du[1] = -0.35*x - 0.75*y + sin(t) + 1
    du[2] = -0.29*x - 0.65*y + cos(t) + 1
end

prob1 = ODEProblem(regular!, [x0, y0], tspan)
sol1 = solve(prob1, Tsit5(), saveat=0.05)

# ============================================================
# 2. Модель регулярные + партизаны
# ============================================================
function mixed!(du, u, p, t)
    x, y = u
    du[1] = -0.24*x - 0.61*y + sin(2t)
    du[2] = -0.3*x*y - 0.71*y + cos(3t)
end

prob2 = ODEProblem(mixed!, [x0, y0], tspan)
sol2 = solve(prob2, Tsit5(), saveat=0.05)

# ============================================================
# Построение графиков
# ============================================================
p1 = plot(sol1, label=["Армия X" "Армия Y"],
          title="1. Регулярные войска",
          xlabel="Время", ylabel="Численность",
          linewidth=2, legend=:topright)

p2 = plot(sol2, label=["Армия X" "Армия Y"],
          title="2. Регулярные + партизаны",
          xlabel="Время", ylabel="Численность",
          linewidth=2, legend=:topright)

# Сохраняем общий график
plot(p1, p2, layout=(2,1), size=(800, 600))
savefig("war_models.png")
cp("war_models.png", "report/war_models.png", force=true)

