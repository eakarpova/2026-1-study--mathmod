using DifferentialEquations, Plots

# Параметры модели (твой вариант 9)
a = 0.21    # коэффициент смертности хищников
b = 0.41    # коэффициент прироста жертв
c = 0.049   # коэффициент увеличения числа хищников за счёт жертв
d = 0.037   # коэффициент смертности жертв от хищников

# Начальные условия
x0 = 14.0   # начальная численность хищников
y0 = 19.0   # начальная численность жертв
u0 = [x0, y0]

# Интервал времени
tspan = (0.0, 100.0)

# ============================================================
# Система уравнений Лотки-Вольтерры
# ============================================================
function lotka_volterra!(du, u, p, t)
    x, y = u
    du[1] = -a*x + c*x*y   # хищники
    du[2] =  b*y - d*x*y   # жертвы
end

# Решение системы
prob = ODEProblem(lotka_volterra!, u0, tspan)
sol = solve(prob, Tsit5(), saveat=0.1)

# ============================================================
# Стационарное состояние
# ============================================================
x_ss = b / d
y_ss = a / c

println("\n" * "="^50)
println("СТАЦИОНАРНОЕ СОСТОЯНИЕ СИСТЕМЫ")
println("="^50)
println("x* (хищники) = b/d = $b / $d = $(round(x_ss, digits=3))")
println("y* (жертвы)  = a/c = $a / $c = $(round(y_ss, digits=3))")
println("="^50)

# ============================================================
# Построение графиков
# ============================================================

# График 1: Зависимость x(t) и y(t) от времени
p1 = plot(sol, label=["Хищники (x)" "Жертвы (y)"],
          title="Динамика численности популяций",
          xlabel="Время", ylabel="Численность",
          linewidth=2, legend=:topright)

# График 2: Фазовый портрет (x от y)
p2 = plot(sol, vars=(1,2), label="Фазовая траектория",
          title="Фазовый портрет (хищники от жертв)",
          xlabel="Жертвы (y)", ylabel="Хищники (x)",
          linewidth=2)

# Добавим стационарную точку на фазовый портрет
scatter!(p2, [y_ss], [x_ss], 
         label="Стационарная точка", 
         color=:red, markersize=8)

# Сохраняем общий график
plot(p1, p2, layout=(2,1), size=(800, 800))
savefig("predator_prey_results.png")
display(plot(p1, p2, layout=(2,1), size=(800, 800)))

println("\n✅ График сохранён в predator_prey_results.png")

# ============================================================
# Сохраняем результаты для отчёта
# ============================================================
using DelimitedFiles

# Создаём папку report, если её нет
mkpath("../report")

# Сохраняем числовые результаты
results = [
    "Стационарное состояние (хищники)"  x_ss
    "Стационарное состояние (жертвы)"   y_ss
    "Начальные условия (хищники)"       x0
    "Начальные условия (жертвы)"        y0
    "Параметр a"  a
    "Параметр b"  b
    "Параметр c"  c
    "Параметр d"  d
]

writedlm("../report/results.csv", results, ',')

